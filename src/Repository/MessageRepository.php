    {
        parent::__construct($registry, Message::class);
    }


    public function findMessage($idE, $idR)
    {
        return $this->getEntityManager()
            ->createQuery(
                'SELECT e
                FROM App\Entity\Message e
                WHERE e.emitter = :idE AND e.receivere = :idR OR e.emitter = :idR AND e.receivere = :idE'
            )
            ->setParameter('idE', $idE)
            ->setParameter('idR', $idR)
            ->getResult();
    }

